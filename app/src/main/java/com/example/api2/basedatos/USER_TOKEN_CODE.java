package com.example.api2.basedatos;

public class USER_TOKEN_CODE {
    private Integer id;
    private String token;

    public USER_TOKEN_CODE(Integer id, String token) {
        this.id = id;
        this.token = token;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
